package util;

public class Calculations {

    public double toAdd(double x, double y) {
        return x + y;
    }

    public double toSubstract(double x, double y) {
        return x - y;
    }

    public double toMultiply(double x, double y) {
        return x * y;
    }

    public double toSplit(double x, double y) {
        return x / y;
    }

    public double calculatePercentage(double x, double y) {
        return x / 100 * y;
    }
}
